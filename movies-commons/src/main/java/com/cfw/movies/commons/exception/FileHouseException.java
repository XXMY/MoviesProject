package com.cfw.movies.commons.exception;

public class FileHouseException extends Exception {

	private static final long serialVersionUID = 5168772551729440562L;

	public FileHouseException() {
		super();
	}

	public FileHouseException(String message, Throwable cause) {
		super(message, cause);
	}

	public FileHouseException(String message) {
		super(message);
	}

	public FileHouseException(Throwable cause) {
		super(cause);
	}
	
}
